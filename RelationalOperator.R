v1 <- c(10, 5, 20, 8)
v2 <- c(10, 15, 5, 8)

# Equality operator (==)
cat ("v1 == v2:", v1 == v2, "\n")

# Not equal operator (!=)
cat ("v1 != v2:", v1 != v2, "\n")

# Less than operator (<)
cat ("v1 < v2:", v1 < v2, "\n")

# Less than or equal to operator (<=)
cat ("v1 <= v2:", v1 <= v2, "\n")

# Greater than operator (>)
cat ("v1 > v2:", v1 > v2, "\n")

# Greater than or equal to operator (>=)
cat ("v1 >= v2:", v1 >= v2, "\n")
